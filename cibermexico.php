						<h3 style="text-align: center;color: black"><b>Ciberseguridad Nacional</b></h3>
						<br>
						<span>Los estados han buscado salvaguardar la ciberseguridad a través de medidas políticas, tecnológicas y estratégicas que incluyen claridad en los conceptos y atribuciones de los actores. Es decir, el concepto de seguridad ha evolucionado para incluir los aspectos tecnológicos que hoy predominan bajo el termino de ciberseguridad nacional. Por ello, los Estados han diseñado ciberestrategias para contrarrestar las diversas amenazas del ciberespacio y los desafíos que deben enfrentar en cuestiones de estrategia, recursos humanos y desarrollo tecnológico.</span>
                        
                        <br><br>
						<span>Desde el contexto de la seguridad nacional, junto con la dependencia en las TIC en los diversos ámbitos del desarrollo de los Estados, existe una necesidad creciente de verificar la implementación segura de los sistemas cibernéticos en campos como la salud, la educación, los servicios gubernamentales o la industria; es decir se deben construir capacidades para la ciberseguridad y la ciberdefensa con base en políticas públicas de largo alcance, así como en instituciones de carácter permanente que den vida a un sistema de ciberseguridad nacional.</span>

						<br><br>
						<h3 style="color: black; font-style: italic; font-size: 20px"><b>De la seguridad física a la seguridad virtual</b></h3>
						<br>
						<span>En el contexto internacional, donde las TIC (Tecnologías de la Información y Comunicación) tienen un papel muy importante es preciso contar con un concepto y una estrategia de seguridad nacional que implementen acciones políticas y jurídicas para salvaguardar los recursos materiales en todos los ámbitos de combate desde una perspectiva multidisciplinaria, multidimensional y multinivel. Recordando que, si bien la definición tradicional de seguridad nacional funcionó antes y durante la Guerra Fría, pero después del fin de esta lucha ideológica el concepto de seguridad muestra huellas de cansancio, por esta razón a nivel internacional existe un debate sobre la integración de nuevos actores, factores y temas para reconceptualizar la palabra seguridad. Contar con una definición precisa de lo que se entiende por seguridad evita la incertidumbre y establece tanto los límites como los objetivos que debe cumplir.</span>

						<br><br>
						<span>En el caso particular de México la definición de seguridad nacional aparece oficialmente hasta principios de los años 80´s. De acuerdo con lo presentado en la obra Grandes temas del constitucionalismo mexicano publicada por la Corte Suprema de Justicia de la Nación (2005) su evolución ha sido la siguiente:</span>

						<br><br>
						<span style="font-style: italic; font-weight: bold"> El tema de la seguridad nacional se trató por primera vez en el Plan Global de Desarrollo 1980-1982 como tema de interés de Estado. Y se clasificó ésta como “la herramienta para mantener la condición de libertad, paz y justicia social dentro del marco institucional”. El régimen le otorgó a las fuerzas armadas, el rol de “colaborar y coadyuvar” en la seguridad nacional. En el plan de 1989-1994, se la consideró como “condición imprescindible para el mantenimiento del orden soberano, por lo que debe ser preservada tanto en el ámbito interno como en el de las relaciones internacionales, con base en la concertación interna y la negociación externa”. En el plan 1995-2000 se mantuvo la definición apuntada y se materializó una política de seguridad nacional, cuyo principio rector consiste en el fortalecimiento de la soberanía a través de acciones internas y externas, mientras que su sucesor, el Plan 2001-2006, estimó que el tema “tiene como metas principales velar por la protección y preservación del interés colectivo, evitando en lo posible o minimizando cualquier riesgo o amenaza a la integridad física de la población y de las instituciones” (p. 86).</span>

