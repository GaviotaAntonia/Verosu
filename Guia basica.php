						<h3 style="text-align: center;color: black"><b>Ciberseguridad</b></h3>
						<br>
						<span>Los elementos y las diferentes áreas de la ciberseguridad pueden parecer interminables, pero todas ellas son vulnerables a una práctica delictiva común que es consumada a través del ciberespacio, potenciando así el daño que un delincuente del ciberespacio o ciberdelincuente puede provocar, de tal forma que una persona puede cometer varios ilícitos sin necesidad de moverse de su sitio y a su vez tener efectos multiplicadores adversos en las personas, instalaciones y en el país.</span>
                        
                        <br><br>
						<span>Por lo anterior, la cultura de la prevención se convierte en uno de los recursos más importantes para evitar afectaciones por la ciberdelincuencia, hoy día se han acuñado conceptos como civismo digital, donde se espera que todos los usuarios de la Internet sean más cuidadosos en su actuar dentro del ciberespacio, recordando que la experiencia en tecnología no es equivalente a la experiencia en seguridad de tecnología, por lo que la presente guía plantea un punto de referencia para apoyar la seguridad. </span>
						
						<br><br>
						<h3 style="color: black; font-size: 17px"><b>Objetivo de esta guía</b></h3>
						<span>El propósito de esta guía es informar y brindar un apoyo básico en la habilitación de controles y buenas prácticas para el fortalecimiento de la seguridad cibernetica, así como también ayudar a prevenir e identificar vulnerabilidades de los sistemas electrónicos de seguridad a fin de asegurar la protección de personas, información, procesos e infraestructura, con la finalidad de asegurar el correcto funcionamiento de los sistemas informáticos del individuo, negocio o empresa.</span>

